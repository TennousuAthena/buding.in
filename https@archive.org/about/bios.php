<p>In the early days of the net he managed technology and business development at The WELL and lead their effort to build the first web-based interface for online forums, and also helped bring the pre-web Internet to millions of people by running AOL's Gopher project as part of their Internet Center. He managed technology for the pioneering US-Soviet Sovam Teleport email service and co-founded and managed PeaceNet, one of the first online communities for progressive social change, and later IGC.org, one of the world first ISPs. He also co-founded the global NGO, APC.org. Mark's early training and experience with computer-mediated communications was acquired while he served in the US Air Force, spending more than 3 years working at the Air Force Data Services Center at the Pentagon. Mark's nonprofit work includes volunteering with the open education library http://oercommons.org and as a board member of http://openrecoverysf.org.
