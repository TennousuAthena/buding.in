      <div>
<img src="../download/images/BrewsterKahleHeadshot.png"> <a name="brewster" id="brewster"></a><b>Brewster Kahle</b><i>, Founder &amp; Digital Librarian, Internet Archive</i><br>

<p>A passionate advocate for public Internet access and a successful entrepreneur, Brewster Kahle has spent his career intent on a singular focus: providing Universal Access to All Knowledge.  He is the founder and Digital Librarian of the Internet Archive, one of the largest libraries in the world.  Soon after graduating from the Massachusetts Institute of Technology where he studied artificial intelligence, Kahle helped found the company Thinking Machines, a parallel supercomputer maker.  In 1989, Kahle created the Internet's first publishing system called Wide Area Information Server (WAIS), later selling the company to AOL. In 1996, Kahle co-founded Alexa Internet, which helps catalog the Web, selling it to Amazon.com in 1999.  The Internet Archive, which he founded in 1996, now preserves 20 petabytes of data - the books, Web pages, music, television, and software of our cultural heritage, working with more than 400 library and university partners to create a digital library, accessible to all.</p>
