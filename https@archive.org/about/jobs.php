<a id="webcrawl"></a><h1 align="center"> Web Crawl Engineer
 </h1>
<p><strong>Location: </strong>Inner Richmond, San Francisco, CA or Remote</p>
<p><strong>Job Classification: </strong>: Full-time, Exempt</p>
<p><strong>Job Summary: </strong>The Internet Archive is seeking a Web Crawl Engineer for its Web Archiving Group. Our crawl engineering team is responsible for capturing and managing the highest quality content from the web. An ideal candidate demonstrates independence and initiative, is a problem solver, works well autonomously, and is technologically savvy. Additionally, the ideal candidate is open to being trained on, and helping advance, best practices and standards around large-scale web harvests, web data processing and engineering, and contributing to the development of new harvesting, access, and analysis tools.
<p>
The position will work in the Web Archiving Group in support of web harvesting services and programs working with partners ranging from national libraries and archives to collaborative international initiatives supporting the collection, preservation, and accessibility of web content. The role will help design the strategy and implementation of web archiving services using open source technologies and platforms, develop harvest techniques and tools to enable archival capture and re-rendering of rich media, streaming content, social media, as well as traditional web page content. The position will also create tools, services, and workflows to improve crawl analysis, reports, data management and derivation, and identify technical, operational and data analysis requirements. This role contributes to defining deployment architectures and workflows, managing data at scale, and monitoring production systems.
<strong></strong></p></p>
<p><strong>Essential Job Functions: </strong></p>
<ul>
<li>Running large-scale web harvests on global and national domain levels and focused and specialized crawls using Heritrix, our open-source crawler, as well as other open-source technologies developed internally, including Umbra, Brozzler, warcprox and others.
<li>Configuration, monitoring, and improvement of large-scale, multi-machine web crawls to ensure their quality and timely completion.
<li>Processing, analysis and quality assurance of archived web content to ensure it is complete and of the highest quality.
<li>Contribute to development of tools for automated analysis and reporting of crawl material, and to development projects focused on crawling, processing, and access.
<li>Manage both large ingests and exports of web data, derivatives, logs, and reports.
<li>Demonstrated experience of delivering on commitments with deadlines and project time lines and working in a collaborative team of engineers and project/product managers.</li>

</ul>
<p></p>
<p><strong>Minimum Qualifications:</strong></p>
<ul>
<li>Experience with web crawlers or scrapers, especially Heritrix
<li>Proven experience in Unix shell scripting and Python coding required
<li>Solid experience in Internet protocols (HTTP is must.) Strong knowledge of HTML, JavaScript and Web technologies in general
<li>Knowledge of building and deploying web applications, databases, web-host services, and knowledge of basic Linux system administration
<li>Ability to work in, and enjoy, a loosely structured work environment</li>

</ul>
<p></p>
<p><strong>Preferred Qualifications:</strong></p>
<ul>
<li>Cluster computing experience is preferred, especially familiarity with Hadoop and related technologies and tools
<li>Experience or familiarity with Java strongly preferred
<li>Experience with applications designed to display archived web content, especially server-side apps and Wayback
<li>Experience with development environments and system monitoring/administration tools
<li>Experience with open source practices, version control, and code review
<li>Experience with Atlassian tool sets
<li>Flexibility and a sense of humor are a plus</li>
</ul>
<p></p>
<p>Requirements: Bachelor's Degree in Computer Science or a related field, five years of progressively responsible experience in software development. </p>

<p>Reporting Structure: The Web Crawl Engineer reports to the Director of Web Archiving and works closely with other departments. The position works alongside other web archiving engineers as well as program staff in Web Archiving Group and with the broader Internet Archive infrastructure and engineering teams.</p>

<p><strong>To Apply: </strong>Please send your resume and cover letter to <a href="../../../../https@http://mailto_3Ajobs+crawlengineer_40archive.org">jobs+crawlengineer@archive.org</a> with the subject line "Web Crawl Engineer."<em></em></p>
