<h1 align="center">About the Internet Archive</h1></strong></p> 
<p>The Internet Archive is a non-profit with a huge mission: to give everyone access to all knowledge the books, web pages, audio, television and software of our shared human culture.  Forever. Based in San Francisco and with satellites around the world, the Internet Archive staffers are building the digital library of the future--a place where anyone can go to learn and explore. Our 160 engineers, book scanners, librarians, designers and team members have built the 250+ most popular website in the world.  (https://archive.org)
Internet Archive is a non-profit digital library offering free universal access to books, movies &amp; music, as well as 464 billion archived web pages.</p>

